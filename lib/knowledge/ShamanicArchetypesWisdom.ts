/**
 * Detect spirit animal themes in text
 */
export function detectSpiritAnimalThemes(text: string): string[] {
  const animals = Object.keys(SPIRIT_ANIMALS);
  const detected: string[] = [];

  const lowerText = text.toLowerCase();
  for (const animal of animals) {
    if (lowerText.includes(animal)) {
      detected.push(animal);
    }
  }

  return detected;
}

/**
 * Detect archetypal patterns in text
 */
export function detectArchetypalPatterns(text: string): string[] {
  const archetypes = Object.keys(ARCHETYPAL_PATTERNS);
  const detected: string[] = [];

  const lowerText = text.toLowerCase();
  for (const archetype of archetypes) {
    if (lowerText.includes(archetype)) {
      detected.push(archetype);
    }
  }

  return detected;
}

